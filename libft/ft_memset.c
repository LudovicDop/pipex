#include "libft.h"

void	*ft_memset(void *s, int c, size_t n)
{
	size_t	i;
	char	*s_;

	s_ = (char *)s;
	i = 0;
	while (i < n)
	{
		s_[i] = c;
		i++;
	}
	return (s_);
}
